package main

import "fmt"

/*
题目：给定 N，要求输出0，1，2，3 ... N 的每个数的二进制表示里的1的个数。
——————————————————————————————————————————————————————————————————
分析(确定状态):用 f(n) 保存第 n 个数字二进制中1的个数
状态转移方程：f(n) = f(n<<2) + n%2
初始值、边界：f(0) = 0
顺序计算
*/
func main() {
	number := 5
	var f = make([]int, number+1)
	f[0] = 0
	for i := 1; i <= 5; i++ {
		f[i] = f[i<<2] + i%2
	}
	fmt.Println(f)
}
